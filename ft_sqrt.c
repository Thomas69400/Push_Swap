#include "push_swap.h"

int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 0;
	while (i++ < nb)
		if (i * i >= nb)
			return ((int)i);
	return (0);
}
